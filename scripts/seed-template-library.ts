async function seedTemplateLibrary() {
  console.log('🌱 Starting template library seed...');
  console.log(`📦 ${templates.length} templates to seed`);

  let created = 0;
  let skipped = 0;
  let updated = 0;

  for (const template of templates) {
    try {
      // Check if template already exists
      const existing = await prisma.templateLibraryItem.findFirst({
        where: {
          name: template.name,
          jurisdiction: template.jurisdiction,
        },
      });

      if (existing) {
        // Update existing template
        await prisma.templateLibraryItem.update({
          where: { id: existing.id },
          data: {
            description: template.description,
            category: template.category,
            tags: template.tags,
            stateSpecific: template.stateSpecific,
            documentType: template.documentType,
            formSchema: template.formSchema as any,
            formConfig: template.formConfig as any,
            isOfficial: template.isOfficial,
            updatedAt: new Date(),
          },
        });
        updated++;
        console.log(`✏️  Updated: ${template.name}`);
      } else {
        // Create new template
        await prisma.templateLibraryItem.create({
          data: {
            name: template.name,
            description: template.description,
            category: template.category,
            tags: template.tags,
            jurisdiction: template.jurisdiction,
            stateSpecific: template.stateSpecific,
            documentType: template.documentType,
            formSchema: template.formSchema as any,
            formConfig: template.formConfig as any,
            isOfficial: template.isOfficial,
            isActive: true,
            version: 1,
            importCount: 0,
            popularity: 0,
          },
        });
        created++;
        console.log(`✅ Created: ${template.name}`);
      }
    } catch (error) {
      console.error(`❌ Error seeding ${template.name}:`, error);
      skipped++;
    }
  }

  console.log('\n📊 Seed Summary:');
  console.log(`   Created: ${created}`);
  console.log(`   Updated: ${updated}`);
  console.log(`   Skipped: ${skipped}`);
  console.log('✨ Template library seed complete!');
}

// ============================================================================
// MAIN
// ============================================================================

seedTemplateLibrary()
  .catch((error) => {
    console.error('Fatal error during seed:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
