// Helper to get frequently used emojis from localStorage
export function getFrequentlyUsedEmojis(): string[] {
  if (typeof window === "undefined") return DEFAULT_FREQUENT_EMOJIS;

  try {
    const stored = localStorage.getItem("frequentEmojis");
    if (stored) {
      return JSON.parse(stored);
    }
  } catch (error) {
    console.error("Error reading frequent emojis:", error);
  }

  return DEFAULT_FREQUENT_EMOJIS;
}

